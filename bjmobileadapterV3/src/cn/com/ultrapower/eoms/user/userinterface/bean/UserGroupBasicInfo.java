package cn.com.ultrapower.eoms.user.userinterface.bean;

public class UserGroupBasicInfo
{
	//公司名
	private String userCPName;
	//部门名
	private String userDPName;
	//手机号
	private String mobile;
	
	public String getMobile()
	{
		return mobile;
	}
	public void setMobile(String mobile)
	{
		this.mobile = mobile;
	}
	public String getUserCPName()
	{
		return userCPName;
	}
	public void setUserCPName(String userCPName)
	{
		this.userCPName = userCPName;
	}
	public String getUserDPName()
	{
		return userDPName;
	}
	public void setUserDPName(String userDPName)
	{
		this.userDPName = userDPName;
	}
	
	

}
